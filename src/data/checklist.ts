// Helper functions
export function getPhaseItems(phase: ChecklistPhase): ChecklistItem[] {
  return CHECKLIST_ITEMS.filter((item) => item.phase === phase);
}

export function getPhaseInfo(phase: ChecklistPhase): PhaseInfo | undefined {
  return PHASES.find((p) => p.id === phase);
}

export function getItemsBySeverity(
  severity: ChecklistItem["severity"],
): ChecklistItem[] {
  return CHECKLIST_ITEMS.filter((item) => item.severity === severity);
}

export function getAutoDetectableItems(): ChecklistItem[] {
  return CHECKLIST_ITEMS.filter((item) => item.autoDetectable);
}
